class Recruit {
    constructor(id, profession, u_name, pass_hash, full_name, bio, recruit_location, recruit_resume, picture, recruit_status) {
        this.id = id;
        this.profession = profession;
        this.u_name = u_name;
        this.pass_hash = pass_hash;
        this.full_name = full_name;
        this.bio = bio;
        this.recruit_location = recruit_location;
        this.recruit_resume = recruit_resume;
        this.picture = picture;
        this.recruit_status = recruit_status;
    }
}